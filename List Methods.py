# List Methods

colours = ["black", "white", "purple"]
colours.append("red")
print(colours)

colours = ["black", "white", "purple"]
colours.clear()
print(colours)

colours = ["black", "white", "purple"]
a = colours.copy()
print(a)

colours = ["black", "white", "purple"]
b = colours.count("purple")
print(b)

colours = ["black","white","purple"]
cars = ['Ford', 'BMW', 'Volvo']
colours.extend(cars)
print(colours)

colours = ["black","white","purple"]
x = colours.index("black")
print(x)

colours = ["black","white","purple"]
colours.insert(1, "red")
print(colours)

colours = ["black","white","purple"]
colours.pop(0)
print(colours)

colours = ["black","white","purple"]
colours.remove("white")
print(colours)

colours = ["black","white","purple"]
colours.reverse()
print(colours)

colours = ["black","white","purple"]
colours.sort()
print(colours)


